pub fn next(self: *Tokenizer) Token {
    var state: enum {
        start,
        cr,
        back_slash,
        back_slash_cr,
        u,
        u8,
        U,
        L,
        string_literal,
        char_literal_start,
        char_literal,
        escape_sequence,
        cr_escape,
        octal_escape,
        hex_escape,
        unicode_escape,
        identifier,
        extended_identifier,
        equal,
        bang,
        pipe,
        percent,
        asterisk,
        plus,
        angle_bracket_left,
        angle_bracket_angle_bracket_left,
        angle_bracket_right,
        angle_bracket_angle_bracket_right,
        caret,
        period,
        period2,
        minus,
        slash,
        ampersand,
        hash,
        line_comment,
        multi_line_comment,
        multi_line_comment_asterisk,
        zero,
        integer_literal_oct,
        integer_literal_binary,
        integer_literal_binary_first,
        integer_literal_hex,
        integer_literal_hex_first,
        integer_literal,
        integer_suffix,
        integer_suffix_u,
        integer_suffix_l,
        integer_suffix_ll,
        integer_suffix_ul,
        float_fraction,
        float_fraction_hex,
        float_exponent,
        float_exponent_digits,
        float_suffix,
    } = .start;

    var start = self.index;
    var id: Token.Id = .eof;

    var string = false;
    var counter: u32 = 0;
    var codepoint_len: u3 = undefined;
    while (self.index < self.buf.len) : (self.index += codepoint_len) {
        codepoint_len = std.unicode.utf8ByteSequenceLength(self.buf[self.index]) catch unreachable;
        const c = std.unicode.utf8Decode(self.buf[self.index .. self.index + codepoint_len]) catch unreachable;
        switch (state) {
            .start => switch (c) {
                '\n' => {
                    id = .nl;
                    self.index += 1;
                    break;
                },
                '\r' => state = .cr,
                '"' => {
                    id = .string_literal;
                    state = .string_literal;
                },
                '\'' => {
                    id = .char_literal;
                    state = .char_literal_start;
                },
                'u' => state = .u,
                'U' => state = .U,
                'L' => state = .L,
                'a'...'t', 'v'...'z', 'A'...'K', 'M'...'T', 'V'...'Z', '_', '$' => state = .identifier,
                '=' => state = .equal,
                '!' => state = .bang,
                '|' => state = .pipe,
                '(' => {
                    id = .l_paren;
                    self.index += 1;
                    break;
                },
                ')' => {
                    id = .r_paren;
                    self.index += 1;
                    break;
                },
                '[' => {
                    id = .l_bracket;
                    self.index += 1;
                    break;
                },
                ']' => {
                    id = .r_bracket;
                    self.index += 1;
                    break;
                },
                ';' => {
                    id = .semicolon;
                    self.index += 1;
                    break;
                },
                ',' => {
                    id = .comma;
                    self.index += 1;
                    break;
                },
                '?' => {
                    id = .question_mark;
                    self.index += 1;
                    break;
                },
                ':' => {
                    id = .colon;
                    self.index += 1;
                    break;
                },
                '%' => state = .percent,
                '*' => state = .asterisk,
                '+' => state = .plus,
                '<' => state = .angle_bracket_left,
                '>' => state = .angle_bracket_right,
                '^' => state = .caret,
                '{' => {
                    id = .l_brace;
                    self.index += 1;
                    break;
                },
                '}' => {
                    id = .r_brace;
                    self.index += 1;
                    break;
                },
                '~' => {
                    id = .tilde;
                    self.index += 1;
                    break;
                },
                '.' => state = .period,
                '-' => state = .minus,
                '/' => state = .slash,
                '&' => state = .ampersand,
                '#' => state = .hash,
                '0' => state = .zero,
                '1'...'9' => state = .integer_literal,
                '\\' => state = .back_slash,
                '\t', '\x0B', '\x0C', ' ' => start = self.index + 1,
                else => {
                    if (c > 0x7F and Token.mayAppearInIdent(self.comp, c, .start)) {
                        state = .extended_identifier;
                    } else {
                        id = .invalid;
                        self.index += codepoint_len;
                        break;
                    }
                },
            },
            .cr => switch (c) {
                '\n' => {
                    id = .nl;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .nl;
                    break;
                },
            },
            .back_slash => switch (c) {
                '\n' => {
                    start = self.index + 1;
                    state = .start;
                },
                '\r' => state = .back_slash_cr,
                '\t', '\x0B', '\x0C', ' ' => {
                    // TODO warn
                },
                else => {
                    id = .invalid;
                    break;
                },
            },
            .back_slash_cr => switch (c) {
                '\n' => {
                    start = self.index + 1;
                    state = .start;
                },
                else => {
                    start = self.index;
                    state = .start;
                },
            },
            .u => switch (c) {
                '8' => {
                    state = .u8;
                },
                '\'' => {
                    id = .char_literal_utf_16;
                    state = .char_literal_start;
                },
                '\"' => {
                    id = .string_literal_utf_16;
                    state = .string_literal;
                },
                else => {
                    codepoint_len = 0;
                    state = .identifier;
                },
            },
            .u8 => switch (c) {
                '\"' => {
                    id = .string_literal_utf_8;
                    state = .string_literal;
                },
                else => {
                    codepoint_len = 0;
                    state = .identifier;
                },
            },
            .U => switch (c) {
                '\'' => {
                    id = .char_literal_utf_32;
                    state = .char_literal_start;
                },
                '\"' => {
                    id = .string_literal_utf_32;
                    state = .string_literal;
                },
                else => {
                    codepoint_len = 0;
                    state = .identifier;
                },
            },
            .L => switch (c) {
                '\'' => {
                    id = .char_literal_wide;
                    state = .char_literal_start;
                },
                '\"' => {
                    id = .string_literal_wide;
                    state = .string_literal;
                },
                else => {
                    codepoint_len = 0;
                    state = .identifier;
                },
            },
            .string_literal => switch (c) {
                '\\' => {
                    string = true;
                    state = .escape_sequence;
                },
                '"' => {
                    self.index += 1;
                    break;
                },
                '\n', '\r' => {
                    id = .invalid;
                    break;
                },
                else => {},
            },
            .char_literal_start => switch (c) {
                '\\' => {
                    string = false;
                    state = .escape_sequence;
                },

                '\'', '\n' => {
                    id = .invalid;
                    break;
                },
                else => {
                    state = .char_literal;
                },
            },
            .char_literal => switch (c) {
                '\\' => {
                    string = false;
                    state = .escape_sequence;
                },
                '\'' => {
                    self.index += 1;
                    break;
                },
                '\n' => {
                    id = .invalid;
                    break;
                },
                else => {},
            },
            .escape_sequence => switch (c) {
                '\'', '"', '?', '\\', 'a', 'b', 'e', 'f', 'n', 'r', 't', 'v', '\n' => {
                    state = if (string) .string_literal else .char_literal;
                },
                '\r' => state = .cr_escape,
                '0'...'7' => {
                    counter = 1;
                    state = .octal_escape;
                },
                'x' => state = .hex_escape,
                'u' => {
                    counter = 4;
                    state = .unicode_escape;
                },
                'U' => {
                    counter = 8;
                    state = .unicode_escape;
                },
                else => {
                    id = .invalid;
                    break;
                },
            },
            .cr_escape => switch (c) {
                '\n' => {
                    state = if (string) .string_literal else .char_literal;
                },
                else => {
                    id = .invalid;
                    break;
                },
            },
            .octal_escape => switch (c) {
                '0'...'7' => {
                    counter += 1;
                    if (counter == 3) {
                        state = if (string) .string_literal else .char_literal;
                    }
                },
                else => {
                    codepoint_len = 0;
                    state = if (string) .string_literal else .char_literal;
                },
            },
            .hex_escape => switch (c) {
                '0'...'9', 'a'...'f', 'A'...'F' => {},
                else => {
                    codepoint_len = 0;
                    state = if (string) .string_literal else .char_literal;
                },
            },
            .unicode_escape => switch (c) {
                '0'...'9', 'a'...'f', 'A'...'F' => {
                    counter -= 1;
                    if (counter == 0) {
                        state = if (string) .string_literal else .char_literal;
                    }
                },
                else => {
                    id = .invalid;
                    break;
                },
            },
            .identifier, .extended_identifier => switch (c) {
                'a'...'z', 'A'...'Z', '_', '0'...'9', '$' => {},
                else => {
                    if (c <= 0x7F or !Token.mayAppearInIdent(self.comp, c, .inside)) {
                        id = if (state == .identifier) Token.getTokenId(self.comp, self.buf[start..self.index]) else .extended_identifier;
                        break;
                    }
                    state = .extended_identifier;
                },
            },
            .equal => switch (c) {
                '=' => {
                    id = .equal_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .equal;
                    break;
                },
            },
            .bang => switch (c) {
                '=' => {
                    id = .bang_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .bang;
                    break;
                },
            },
            .pipe => switch (c) {
                '=' => {
                    id = .pipe_equal;
                    self.index += 1;
                    break;
                },
                '|' => {
                    id = .pipe_pipe;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .pipe;
                    break;
                },
            },
            .percent => switch (c) {
                '=' => {
                    id = .percent_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .percent;
                    break;
                },
            },
            .asterisk => switch (c) {
                '=' => {
                    id = .asterisk_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .asterisk;
                    break;
                },
            },
            .plus => switch (c) {
                '=' => {
                    id = .plus_equal;
                    self.index += 1;
                    break;
                },
                '+' => {
                    id = .plus_plus;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .plus;
                    break;
                },
            },
            .angle_bracket_left => switch (c) {
                '<' => {
                    state = .angle_bracket_angle_bracket_left;
                },
                '=' => {
                    id = .angle_bracket_left_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .angle_bracket_left;
                    break;
                },
            },
            .angle_bracket_angle_bracket_left => switch (c) {
                '=' => {
                    id = .angle_bracket_angle_bracket_left_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .angle_bracket_angle_bracket_left;
                    break;
                },
            },
            .angle_bracket_right => switch (c) {
                '>' => {
                    state = .angle_bracket_angle_bracket_right;
                },
                '=' => {
                    id = .angle_bracket_right_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .angle_bracket_right;
                    break;
                },
            },
            .angle_bracket_angle_bracket_right => switch (c) {
                '=' => {
                    id = .angle_bracket_angle_bracket_right_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .angle_bracket_angle_bracket_right;
                    break;
                },
            },
            .caret => switch (c) {
                '=' => {
                    id = .caret_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .caret;
                    break;
                },
            },
            .period => switch (c) {
                '.' => {
                    state = .period2;
                },
                '0'...'9' => {
                    state = .float_fraction;
                },
                else => {
                    id = .period;
                    break;
                },
            },
            .period2 => switch (c) {
                '.' => {
                    id = .ellipsis;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .period;
                    self.index -= 1;
                    break;
                },
            },
            .minus => switch (c) {
                '>' => {
                    id = .arrow;
                    self.index += 1;
                    break;
                },
                '=' => {
                    id = .minus_equal;
                    self.index += 1;
                    break;
                },
                '-' => {
                    id = .minus_minus;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .minus;
                    break;
                },
            },
            .ampersand => switch (c) {
                '&' => {
                    id = .ampersand_ampersand;
                    self.index += 1;
                    break;
                },
                '=' => {
                    id = .ampersand_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .ampersand;
                    break;
                },
            },
            .hash => switch (c) {
                '#' => {
                    id = .hash_hash;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .hash;
                    break;
                },
            },
            .slash => switch (c) {
                '/' => {
                    state = .line_comment;
                },
                '*' => {
                    state = .multi_line_comment;
                },
                '=' => {
                    id = .slash_equal;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .slash;
                    break;
                },
            },
            .line_comment => switch (c) {
                '\n' => {
                    self.index -= 1;
                    state = .start;
                },
                else => {},
            },
            .multi_line_comment => switch (c) {
                '*' => state = .multi_line_comment_asterisk,
                else => {},
            },
            .multi_line_comment_asterisk => switch (c) {
                '/' => state = .start,
                else => state = .multi_line_comment,
            },
            .zero => switch (c) {
                '0'...'9' => state = .integer_literal_oct,
                'b', 'B' => state = .integer_literal_binary_first,
                'x', 'X' => state = .integer_literal_hex_first,
                '.' => state = .float_fraction,
                else => {
                    if (c <= 0x7F) {
                        state = .integer_suffix;
                        self.index -= 1;
                    } else {
                        id = .integer_literal;
                        break;
                    }
                },
            },
            .integer_literal_oct => switch (c) {
                '0'...'7' => {},
                else => {
                    if (c <= 0x7F) {
                        state = .integer_suffix;
                        self.index -= 1;
                    } else {
                        id = .integer_literal;
                        break;
                    }
                },
            },
            .integer_literal_binary_first => switch (c) {
                '0', '1' => state = .integer_literal_binary,
                else => {
                    id = .invalid;
                    break;
                },
            },
            .integer_literal_binary => switch (c) {
                '0', '1' => {},
                else => {
                    if (c <= 0x7F) {
                        state = .integer_suffix;
                        self.index -= 1;
                    } else {
                        id = .integer_literal;
                        break;
                    }
                },
            },
            .integer_literal_hex_first => switch (c) {
                '0'...'9', 'a'...'f', 'A'...'F' => state = .integer_literal_hex,
                '.' => state = .float_fraction_hex,
                'p', 'P' => state = .float_exponent,
                else => {
                    id = .invalid;
                    break;
                },
            },
            .integer_literal_hex => switch (c) {
                '0'...'9', 'a'...'f', 'A'...'F' => {},
                '.' => state = .float_fraction_hex,
                'p', 'P' => state = .float_exponent,
                else => {
                    if (c <= 0x7F) {
                        state = .integer_suffix;
                        self.index -= 1;
                    } else {
                        id = .integer_literal;
                        break;
                    }
                },
            },
            .integer_literal => switch (c) {
                '0'...'9' => {},
                '.' => state = .float_fraction,
                'e', 'E' => state = .float_exponent,
                else => {
                    if (c <= 0x7F) {
                        state = .integer_suffix;
                        self.index -= 1;
                    } else {
                        id = .integer_literal;
                        break;
                    }
                },
            },
            .integer_suffix => switch (c) {
                'u', 'U' => state = .integer_suffix_u,
                'l', 'L' => state = .integer_suffix_l,
                else => {
                    id = .integer_literal;
                    break;
                },
            },
            .integer_suffix_u => switch (c) {
                'l', 'L' => state = .integer_suffix_ul,
                else => {
                    id = .integer_literal_u;
                    break;
                },
            },
            .integer_suffix_l => switch (c) {
                'l', 'L' => state = .integer_suffix_ll,
                'u', 'U' => {
                    id = .integer_literal_lu;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .integer_literal_l;
                    break;
                },
            },
            .integer_suffix_ll => switch (c) {
                'u', 'U' => {
                    id = .integer_literal_llu;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .integer_literal_ll;
                    break;
                },
            },
            .integer_suffix_ul => switch (c) {
                'l', 'L' => {
                    id = .integer_literal_llu;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .integer_literal_lu;
                    break;
                },
            },
            .float_fraction => switch (c) {
                '0'...'9' => {},
                'e', 'E' => state = .float_exponent,
                else => {
                    if (c <= 0x7F) {
                        self.index -= 1;
                        state = .float_suffix;
                    } else {
                        id = .float_literal;
                        break;
                    }
                },
            },
            .float_fraction_hex => switch (c) {
                '0'...'9', 'a'...'f', 'A'...'F' => {},
                'p', 'P' => state = .float_exponent,
                else => {
                    id = .invalid;
                    break;
                },
            },
            .float_exponent => switch (c) {
                '+', '-' => state = .float_exponent_digits,
                else => {
                    codepoint_len = 0;
                    state = .float_exponent_digits;
                },
            },
            .float_exponent_digits => switch (c) {
                '0'...'9' => counter += 1,
                else => {
                    if (counter == 0) {
                        id = .invalid;
                        break;
                    }
                    codepoint_len = 0;
                    state = .float_suffix;
                },
            },
            .float_suffix => switch (c) {
                'l', 'L' => {
                    id = .float_literal_l;
                    self.index += 1;
                    break;
                },
                'f', 'F' => {
                    id = .float_literal_f;
                    self.index += 1;
                    break;
                },
                else => {
                    id = .float_literal;
                    break;
                },
            },
        }
    } else if (self.index == self.buf.len) {
        switch (state) {
            .start, .line_comment => {},
            .u, .u8, .U, .L, .identifier => id = Token.getTokenId(self.comp, self.buf[start..self.index]),
            .extended_identifier => id = .extended_identifier,
            .cr => id = .nl,
            .back_slash, .back_slash_cr => {},
            .period2,
            .string_literal,
            .char_literal_start,
            .char_literal,
            .escape_sequence,
            .cr_escape,
            .octal_escape,
            .hex_escape,
            .unicode_escape,
            .multi_line_comment,
            .multi_line_comment_asterisk,
            .float_exponent,
            .integer_literal_binary_first,
            .integer_literal_hex_first,
            => id = .invalid,

            .float_exponent_digits => id = if (counter == 0) .invalid else .float_literal,
            .float_fraction,
            .float_fraction_hex,
            => id = .float_literal,
            .integer_literal_oct,
            .integer_literal_binary,
            .integer_literal_hex,
            .integer_literal,
            .integer_suffix,
            .zero,
            => id = .integer_literal,
            .integer_suffix_u => id = .integer_literal_u,
            .integer_suffix_l => id = .integer_literal_l,
            .integer_suffix_ll => id = .integer_literal_ll,
            .integer_suffix_ul => id = .integer_literal_lu,

            .float_suffix => id = .float_literal,
            .equal => id = .equal,
            .bang => id = .bang,
            .minus => id = .minus,
            .slash => id = .slash,
            .ampersand => id = .ampersand,
            .hash => id = .hash,
            .period => id = .period,
            .pipe => id = .pipe,
            .angle_bracket_angle_bracket_right => id = .angle_bracket_angle_bracket_right,
            .angle_bracket_right => id = .angle_bracket_right,
            .angle_bracket_angle_bracket_left => id = .angle_bracket_angle_bracket_left,
            .angle_bracket_left => id = .angle_bracket_left,
            .plus => id = .plus,
            .percent => id = .percent,
            .caret => id = .caret,
            .asterisk => id = .asterisk,
        }
    }

    return .{
        .id = id,
        .start = start,
        .end = self.index,
        .source = self.source,
    };
}

